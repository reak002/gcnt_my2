?><p>
	 Сибирский культурный центр является&nbsp;<a href="http://www.omsk-gcnt.ru/ortk">отделом русской традиционной культуры Государственного центра народного творчества</a>. Основной деятельностью отдела&nbsp;является сохранение, изучение и развитие традиционной культуры русского народа. Уважение к своему народу, его прошлому, всей традиционной русской культуре, хранящей вечные сокровища, составляет основу нашего творческого и духовного поиска.
</p>
<p>
	 Сибирский культурный центр является методическим центром для Центров русской традиционной культуры в&nbsp;<a href="http://folk55.ru/etnoturisticheskaya-karta-omskoy-oblasti/index.php">муниципальных районах Омской области</a>, которые с 2002 года созданы и успешно работают практически во всей районах области (30 из 32).
</p>
<p>
	 Центр разрабатывает и реализует межрегиональные и областные проекты по сохранению и развитию русской традиционной культуры и художественных ремёсел. Самыми яркими из них являются Всероссийский фестиваль русской традиционной культуры&nbsp;<a href="http://folk55.ru/egoriy-khorobryy/index.php">«Егорий Хоробрый»</a>,&nbsp;межрегиональный съезжий праздник&nbsp;<a href="http://folk55.ru/troitskie-gulyanya/index.php">«Троицкие гулянья»</a> и Всероссийский праздник традиционных ремесел&nbsp;<a href="http://folk55.ru/pokrovskaya-yarmarka/index.php">«Покровская ярмарка»</a>.&nbsp;
</p>
<p>
	 Одним из основных направлений деятельности является научно-исследовательская экспедиционная деятельность по сбору фольклора и этнографии славянских групп населения Омской области, создание единого областного фольклорного архива, его обработка, систематизация и хранение. Специалисты Сибирского культурного центра ведут активную методическую работу с&nbsp;<a href="http://folk55.ru/tsentry/tsentry-russkoy-traditsionnoy-kultury-omskoy-oblasti.php">центрами русской традиционной культуры Омской области</a>.
</p>
